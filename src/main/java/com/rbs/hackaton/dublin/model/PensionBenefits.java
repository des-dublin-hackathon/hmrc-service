package com.rbs.hackaton.dublin.model;

public class PensionBenefits {
    private Double otherPensionsAndRetirementAnnuities;
    private Double incapacityBenefit;
    private Double jobseekersAllowance;

    public Double getOtherPensionsAndRetirementAnnuities() {
        return otherPensionsAndRetirementAnnuities;
    }

    public void setOtherPensionsAndRetirementAnnuities(Double otherPensionsAndRetirementAnnuities) {
        this.otherPensionsAndRetirementAnnuities = otherPensionsAndRetirementAnnuities;
    }

    public Double getIncapacityBenefit() {
        return incapacityBenefit;
    }

    public void setIncapacityBenefit(Double incapacityBenefit) {
        this.incapacityBenefit = incapacityBenefit;
    }

    public Double getJobseekersAllowance() {
        return jobseekersAllowance;
    }

    public void setJobseekersAllowance(Double jobseekersAllowance) {
        this.jobseekersAllowance = jobseekersAllowance;
    }
}
